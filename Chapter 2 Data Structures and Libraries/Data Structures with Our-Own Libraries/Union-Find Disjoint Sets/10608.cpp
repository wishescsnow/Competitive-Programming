#include <iostream>
#include <vector>

using namespace std;
typedef vector<int> vi;

class UnionFind{
private: vi p, rank, size; int numSet;
public:
	UnionFind(int N){
		numSet = N;
		size.assign(N, 1);
		rank.assign(N, 0);
		p.assign(N, 0);
		for(int i=0;i<N;i++)
			p[i] = i;
	}

	int findSet(int i) {
		return (p[i] == i) ? i : (p[i] = findSet(p[i]));
	}

	bool isSameSet(int i, int j){
		return (findSet(i) == findSet(j));
	}

	void unionSet(int i, int j){
		if(!isSameSet(i, j)){
			numSet--;
			int x = findSet(i), y = findSet(j);
			if(rank[x] > rank[y])
			{
				size[x] += size[y];
				p[y] = x;
			}
			else{
				size[y] += size[x];
				p[x] = y;
				if(rank[x] == rank[y])
					rank[y]++;
			}
		}
	}

	int numDisjointSets(){
		return numSet;
	}

	int sizeOfSet(int i){
		return size[findSet(i)];
	}
};

int main()
{
	int n, N, M, A, B, ans;
	cin >> n;
	while(n--)
	{
		cin >> N >> M;
		UnionFind uf(N);
		for(int i=0;i<M;i++)
		{
			cin >> A >> B;
			uf.unionSet(A-1, B-1);
		}

		ans = 0;
		for(int i=0;i<N;i++)
			ans = max(ans, uf.sizeOfSet(i));

		cout << ans << endl;
	}
	return 0;
}